def parse_data(raw_data):
    """تحليل البيانات الخام إلى قائمة من القواميس"""
    events = []
    lines = raw_data.strip().split('\n')
    
    for line in lines:
        if not line.strip():
            continue
        
        parts = line.split('\t')
        if len(parts) >= 7:
            try:
                event = {
                    'timestamp': parts[0].strip(),
                    'title': parts[1].strip(),
                    'source': parts[2].strip(),
                    'category': parts[3].strip(),
                    'region': parts[4].strip(),
                    'actor': parts[5].strip(),
                    'score': int(parts[6].strip()) if parts[6].strip().isdigit() else 0
                }
                events.append(event)
            except Exception as e:
                print(f"خطأ في تحليل السطر: {line[:50]}... - {e}")
    
    return events

def analyze_regions(events):
    """تحليل الأحداث حسب المنطقة"""
    region_counter = Counter(event['region'] for event in events)
    return region_counter

def analyze_actors(events):
    """تحليل الأحداث حسب الفاعلين"""
    actor_counter = Counter(event['actor'] for event in events)
    return actor_counter

def analyze_categories(events):
    """تحليل الأحداث حسب التصنيف"""
    category_counter = Counter(event['category'] for event in events)
    return category_counter

def analyze_sources(events):
    """تحليل الأحداث حسب المصدر"""
    source_counter = Counter(event['source'] for event in events)
    return source_counter

def extract_military_events(events):
    """استخراج الأحداث العسكرية/الأمنية"""
    military_events = [e for e in events if e['category'] == 'عسكري/أمني']
    return military_events

def extract_keywords(text):
    """استخراج كلمات مفتاحية من النص"""
    keywords = {
        'غارة جوية': ['غارة جوية', 'طيران حربي', 'طائرات'],
        'قصف مدفعي': ['قصف مدفعي', 'مدفعية'],
        'توغل': ['توغل', 'آليات'],
        'استهداف': ['استهدف', 'استهداف'],
        'مفاوضات': ['مفاوضات', 'وفد', 'اجتماع'],
        'بيان': ['بيان', 'تصريح'],
        'شهيد': ['شهيد', 'شهداء', 'جرحى'],
        'طائرة مسيرة': ['مسيّرة', 'طائرة مسيرة', 'درون']
    }
    
    found_keywords = []
    for keyword, patterns in keywords.items():
        for pattern in patterns:
            if pattern in text:
                found_keywords.append(keyword)
                break
    
    return found_keywords

def identify_patterns(events):
    """تحديد الأنماط في الأحداث"""
    patterns = {
        'غارات متتالية': [],
        'ردود المقاومة': [],
        'تحركات دبلوماسية': [],
        'تصريحات سياسية': []
    }
    
    for event in events:
        title = event['title']
        
        if any(word in title for word in ['غارة', 'طيران', 'قصف']):
            patterns['غارات متتالية'].append(event)
        
        if any(word in title for word in ['المقاومة', 'حزب الله', 'استهدفنا', 'مجاهدونا']):
            patterns['ردود المقاومة'].append(event)
        
        if any(word in title for word in ['مفاوضات', 'وفد', 'قاليباف', 'باكستان']):
            patterns['تحركات دبلوماسية'].append(event)
        
        if any(word in title for word in ['قال', 'صرح', 'أكد', 'أعلن']):
            patterns['تصريحات سياسية'].append(event)
    
    return patterns

def generate_summary(events, top_n=10):
    """إنشاء ملخص لأهم الأحداث"""
    # ترتيب الأحداث حسب النقاط
    sorted_events = sorted(events, key=lambda x: x['score'], reverse=True)
    
    summary = {
        'total_events': len(events),
        'top_events': sorted_events[:top_n],
        'time_range': {
            'start': sorted_events[-1]['timestamp'] if sorted_events else None,
            'end': sorted_events[0]['timestamp'] if sorted_events else None
        }
    }
    
    return summary

