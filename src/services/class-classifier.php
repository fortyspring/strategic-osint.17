            'الشرطة الإيرانية','المجلس الإعلامي الحكومي الإيراني','علي أكبر ولايتي','الحكومة الإسرائيلية','وزارة الصحة الإسرائيلية',
            'سفير إسرائيل','جيش العدو الإسرائيلي','إعلام إسرائيلي','باكستان','دول البريكس','دول الخليج','الدول العربية',
            'المؤتمر الشعبي اللبناني','إبراهيم الموسوي','حسين الحاج حسن','النائب فضل الله','النائب رعد','ماهر حمود',
            'شاكر البرجاوي','عبد العزيز أبو طالب','نزيه منصور','كتائب القسام (حماس)','المقاومة الإسلامية (حزب الله)',
            'سرايا القدس (الجهاد الإسلامي)','الاتحاد الأوروبي','الناتو','رئيس الناتو','الأمم المتحدة','الأمين العام للأمم المتحدة',
            'البنك الدولي','الكويت','العراق','فايننشال تايمز','بوليتيكو','منظومة الإنذار / الجبهة الداخلية','الوفد الإيراني'
        ];
        
        // الاحتفاظ بالجهات المعروفة المقفلة
        if ($actor !== '' && !in_array($actor, ['غير محدد', 'عام/مجهول', 'فاعل غير محسوم', 'فاعل سياقي', 'فاعل سياقي غير مباشر'], true) && in_array($actor, $knownLockedActors, true)) {
            return $actor;
        }
        
        // قواعد خاصة
        if ($actor === 'منظومة الإنذار / الجبهة الداخلية') return $actor;
        
        // إنذارات الجبهة الداخلية
        if (preg_match('/(لقد انتهى الحدث|انتهى الحدث|انذار احمر|إنذار أحمر|صفارات الإنذار|صافرات الإنذار|الجبهة الداخلية|تسوفار)/ui', $title)) {
            return 'منظومة الإنذار / الجبهة الداخلية';
        }
        
        // غارات إسرائيلية
        if (preg_match('/(الطيران الحربي المعادي|غارة إسرائيلية|غارات إسرائيلية|الجيش الإسرائيلي ينذر|شن غارات|أغار على بلدة|طائرات الاحتلال|الطيران الحربي المعادي أغار|سلسلة غارات إسرائيلية|غارتان لطيران العدو الإسرائيلي|مسيّرات معادية|مسيرات معادية)/ui', $title)) {
            return 'جيش العدو الإسرائيلي';
        }
        
        // سياقات غير عسكرية
        if ($this->isNonMilitaryContext($title) || preg_match('/(صورة\s+لل?وفد|الوفد\s+الإيراني\s+المفاوض|إعلام\s+إيراني|رويترز\s+عن\s+مصدر|مصدر\s+دبلوماسي|المؤتمر الشعبي|النائب فضل الله|نتبلوكس|تغطية\s+خاصة)/ui', $title)) {
            return $namedActor !== '' ? $namedActor : 'فاعل غير محسوم';
        }
        
        // استبدال الجهات غير المؤكدة بأسماء محددة
        if (($actor === 'جيش العدو الإسرائيلي' || $actor === 'فاعل غير محسوم' || $actor === 'فاعل سياقي غير مباشر' || $actor === 'فاعل سياقي') && $namedActor !== '') {
            return $namedActor;
        }
        
        // المقاومة الإسلامية
        if (preg_match('/(المقاومة الإسلامية|حزب الله|استهدفنا|استهدف مجاهدونا|بيان صادر عن المقاومة الإسلامية)/ui', $title)) {
            return 'المقاومة الإسلامية (حزب الله)';
        }
        
        // جيش العدو الإسرائيلي
        if (preg_match('/(الجيش الإسرائيلي|جيش الاحتلال|غارة إسرائيلية|قصف إسرائيلي|اعتداء إسرائيلي|طيران الاحتلال|الطيران الحربي المعادي|طائرات الاحتلال)/ui', $title)) {
            return 'جيش العدو الإسرائيلي';
        }
        
        // وسائل الإعلام
        if (preg_match('/(إعلام|قناة|رويترز|العربية|الميادين|الجزيرة|صحيفة|تغطية|محلل|كاتب|اعلام باكستاني)/ui', $actor)) {
            return $namedActor !== '' ? $namedActor : 'فاعل غير محسوم';
        }
        
        // جهات غير محددة
        if (in_array($actor, ['', 'غير محدد', 'عام/مجهول', 'فاعل غير محسوم', 'فاعل سياقي', 'فاعل سياقي غير مباشر'], true)) {
            if ($namedActor !== '') return $namedActor;
            if (preg_match('/(إيران|ايران|طهران)/ui', $title)) return 'إيران';
            if (preg_match('/(الولايات المتحدة|أمريكا|امريكا|واشنطن|البيت الأبيض|ترامب|ترمب)/ui', $title)) return 'الولايات المتحدة';
            if (preg_match('/(باكستان|إسلام آباد|اسلام آباد)/ui', $title)) return 'باكستان';
            return 'فاعل غير محسوم';
        }
        
        return $actor;
    }
    
    /**
     * تنظيف النص
     * 
     * @param string $text النص الأصلي
     * @return string النص المنظف
     */
    private function cleanText(string $text): string {
        if (!function_exists('so_clean_text')) {
            return trim(preg_replace('/\s+/', ' ', $text));
        }
        return \so_clean_text($text);
    }
    
    /**
     * تطبيع العنوان لإزالة التكرار
     * 
     * @param string $title العنوان
     * @return string العنوان المطبع
     */
    private function normalizeTitleForDedupe(string $title): string {
        if (!function_exists('so_normalize_title_for_dedupe')) {
            return mb_strtolower(trim($title));
        }
        return \so_normalize_title_for_dedupe($title);
    }
    
    /**
     * بناء بصمة العنوان
     * 
     * @param string $title العنوان
     * @return string البصمة
     */
    private function buildTitleFingerprint(string $title): string {
        if (!function_exists('so_build_title_fingerprint')) {
            return md5(mb_strtolower(trim($title)));
        }
        return \so_build_title_fingerprint($title);
    }
    
    /**
     * التحقق من السياق غير العسكري
     * 
     * @param string $text النص
     * @return bool هل هو سياق غير عسكري
     */
    private function isNonMilitaryContext(string $text): bool {
        if (!function_exists('so_is_non_military_context')) {
            $nonMilitaryPatterns = [
                '/(صورة|فيديو|تقرير|تحليل|مقال|رأي|تغطية|مباشرة|مباشر|اجتماع|لقاء|قمة|مؤتمر|مفاوضات|محادثات|زيارة|وفد|بيان صحفي|تصريح|مصدر|رويترز|العربية|الميادين|الجزيرة|باكستان|إسلام آباد|اسلام آباد|ترامب|ترمب|نتنياهو|البيت الأبيض|التلفزيون الإيراني|إيرنا|الرئاسة|تغطية خاصة)/ui'
            ];
            foreach ($nonMilitaryPatterns as $pattern) {
                if (preg_match($pattern, $text)) return true;
            }
            return false;
        }
        return \so_is_non_military_context($text);
    }
    
    /**
     * الحصول على ذاكرة السياق
     * 
     * @return array مصفوفة الأنماط
     */
    private function getContextMemory(): array {
        if ($this->contextMemory === null) {
            if (!function_exists('sod_context_memory_get')) {
                $this->contextMemory = [];
            } else {
                $this->contextMemory = \sod_context_memory_get();
            }
        }
        return $this->contextMemory ?? [];
    }
