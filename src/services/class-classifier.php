    /**
     * استخراج جهة فاعلة مسماة غير عسكرية من النص
     * 
     * @param string $text النص المراد تحليله
     * @return string الجهة الفاعلة المستخرجة أو سلسلة فارغة
     */
    public function extractNamedNonMilitaryActor(string $text): string {
        $text = $this->cleanText($text);
        if ($text === '') return '';
        
        foreach ($this->actorPatterns as $label => $regex) {
            if (preg_match($regex, $text)) {
                return $label;
            }
        }
        
        return '';
    }
    
    /**
     * الاستدلال من ذاكرة السياق
     * 
     * @param string $text النص المراد تحليله
     * @return array مصفوفة نتائج الاستدلال
     */
    public function contextMemoryInfer(string $text): array {
        $text = $this->cleanText($text);
        if ($text === '') return [];
        
        // بناء بصمة العنوان
        $fp = $this->buildTitleFingerprint($text);
        
        // الحصول على الأنماط من الذاكرة
        $patterns = $this->getContextMemory();
        $best = [];
        $bestScore = 0.0;
        
        foreach (array_reverse($patterns) as $entry) {
            if (!is_array($entry)) continue;
            
            $entryTitle = $this->cleanText((string)($entry['title'] ?? ''));
            if ($entryTitle === '') continue;
            
            $score = 0.0;
            
            // مطابقة البصمة
            if ($fp !== '' && !empty($entry['fingerprint']) && $fp === $entry['fingerprint']) {
                $score += 70;
            }
            
            // تشابه النص
            similar_text(
                $this->normalizeTitleForDedupe($text),
                $this->normalizeTitleForDedupe($entryTitle),
                $pct
            );
            $score += ($pct / 100.0) * 40.0;
            
            // خصم للنقاط للجهات غير المحسومة
            if (!empty($entry['actor_v2']) && in_array($entry['actor_v2'], ['فاعل غير محسوم', 'فاعل سياقي', 'فاعل سياقي غير مباشر'], true)) {
                $score -= 35;
            }
            
            if ($score > $bestScore) {
                $bestScore = $score;
                $best = $entry;
            }
        }
        
        if ($bestScore < 60 || empty($best['actor_v2'])) {
            return [];
        }
        
        return [
            'primary_actor' => (string)$best['actor_v2'],
            'secondary_actor' => '',
            'target' => '',
            'confidence' => min(98, (int)round($bestScore)),
            'reason' => 'context-memory-match',
            'actor_matches' => [],
        ];
    }
    
    /**
     * الحاكم الذكي لتحسين النتائج
     * 
     * @param array $result النتائج الحالية
     * @param string $text النص الأصلي
     * @return array النتائج المحسنة
     */
    public function governorAI(array $result, string $text): array {
        $text = $this->cleanText($text);
        
        // التحقق من وجود هجوم
        $hasAttack = preg_match('/(غارة|قصف|استهداف|هجوم|اشتباك|اقتحام|توغل|صاروخ|صواريخ|مسيّرة|مسيرة|دبابة|ثكنة|كمين|أغار|اعتداء إسرائيلي|إطلاق نار|استطلاع بطائرة مسيّرة|تم رصد)/ui', $text) === 1;
        
        // التحقق من السياق غير العسكري
        $isNonMilitary = $this->isNonMilitaryContext($text);
        
        // استخراج جهة مسماة
        $namedActor = $this->extractNamedNonMilitaryActor($text);
        
        // معالجة السياقات غير العسكرية
        if ($isNonMilitary && !$hasAttack) {
            $result['primary_actor'] = $namedActor !== '' ? $namedActor : 'فاعل غير محسوم';
            $result['secondary_actor'] = '';
            $result['target'] = '';
            $result['confidence'] = $namedActor !== '' ? max((int)($result['confidence'] ?? 0), 82) : min((int)($result['confidence'] ?? 35), 45);
            $result['reason'] = $namedActor !== '' ? 'governor-named-nonmilitary' : 'governor-nonmilitary';
            return $result;
        }
        
        // التحقق من المقاومة الإسلامية
        if (preg_match('/(استهدفنا|استهدف مجاهدونا|بيان صادر عن المقاومة الإسلامية|المقاومة الإسلامية في لبنان|المقاومة الاسلامية \(|ثكنة يعرا|مستوطنة أدميت|مستوطنة نهاريا)/ui', $text)) {
            $result['primary_actor'] = 'المقاومة الإسلامية (حزب الله)';
            $result['confidence'] = max((int)($result['confidence'] ?? 0), 90);
            $result['reason'] = 'governor-force-resistance';
        }
        
        // إزالة التصنيفات العسكرية الخاطئة
        if (!$hasAttack && (($result['primary_actor'] ?? '') === 'جيش العدو الإسرائيلي' || ($result['primary_actor'] ?? '') === 'المقاومة الإسلامية (حزب الله)')) {
            $result['primary_actor'] = $namedActor !== '' ? $namedActor : 'فاعل غير محسوم';
            $result['secondary_actor'] = '';
            $result['target'] = '';
            $result['confidence'] = $namedActor !== '' ? 84 : 40;
            $result['reason'] = 'governor-remove-false-military';
        }
        
        return $result;
    }
    
    /**
